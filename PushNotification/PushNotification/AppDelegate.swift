import UIKit

@main
class AppDelegate: UIResponder, UIApplicationDelegate,UNUserNotificationCenterDelegate {

    func userNotificationCenter(_ center: UNUserNotificationCenter, didReceive response: UNNotificationResponse, withCompletionHandler completionHandler: @escaping () -> Void) {
        if response.notification.request.identifier == ViewController.notificationIdentifier{
            
            ViewController.titleNotification = "bildirime Tıklandı."
            
            
        }
        
    }

    func application(_ application: UIApplication, didFinishLaunchingWithOptions launchOptions: [UIApplication.LaunchOptionsKey: Any]?) -> Bool {
        // Override point for customization after application launch.
        
        UNUserNotificationCenter.current().requestAuthorization(options: [.alert, .badge, .sound]) { authorizationStatus, error in
            if authorizationStatus{
                print("Authorization Given")
            }else{
                print("Authorization Rejected")
            }
        }
        
        UNUserNotificationCenter.current().delegate = self
        return true
    }

